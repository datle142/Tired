fun main(){
    // thay  dấu  > thành < ngược lại để thấy khác biệt
    println("do while:")
    var n = 0;
    while(n>5){
        println(n)
        n++
    }

    println("do while:")
    var m = 0
    do{
        println(m)
        m++
    }while(m>5)

    for(num in 1..10){
        print(num )
    }
}